package J03_연산자;

import java.security.Principal;

/*
 * Ctrl+space -> 자동완성
 */
public class Operation1 {

	public static void main(String[] args) {
		int num=50;

		System.out.println(++num);
		System.out.println(num);
		System.out.println(num++);
		System.out.println(num);
		
		System.out.println(--num);
		System.out.println(num);
		System.out.println(num--);
		System.out.println(num);
		
	}

}
